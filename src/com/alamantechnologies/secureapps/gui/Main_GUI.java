    private void addRightMenu()
    {
        tModel = (DefaultTableModel) lockedAppList_JTable.getModel();
        
        popupMenu = new JPopupMenu();
        menuItem_UnLock = new JMenuItem("UnLock");
        menuItem_UnLock.addActionListener(this);
        popupMenu.add(menuItem_UnLock);
        
        menuItem_Lock = new JMenuItem("Lock");
        menuItem_Lock.addActionListener(this);
        popupMenu.add(menuItem_Lock);
        
        lockedAppList_JTable.setComponentPopupMenu(popupMenu);
        lockedAppList_JTable.addMouseListener(new TableMouseListener(lockedAppList_JTable));
        
    }
    
    @Override
    public void actionPerformed(ActionEvent e) {
        int row;
        
        JMenuItem menu = (JMenuItem) e.getSource();
        
        if(menu == menuItem_UnLock)
        {
            row = lockedAppList_JTable.getSelectedRow();
            String fileLocation = "";
            String encKey = "";

            fileLocation = String.valueOf(lockedAppList_JTable.getValueAt(row, 2));
            if(LockHandler.unLockApp(fileLocation))
            {
                Gen_Util.showSuccessDialog(rootPane, "Successfully UnLocked!");
            }
            LockHandler.getLockedAppData(lockedAppList_JTable);
        }
        if(menu == menuItem_Lock)
        {
            row = lockedAppList_JTable.getSelectedRow();
            String fileLocation = "";
            String encKey = "";

            fileLocation = String.valueOf(lockedAppList_JTable.getValueAt(row, 2));
            if(LockHandler.lockApp(fileLocation))
            {
                Gen_Util.showSuccessDialog(rootPane, "Successfully Locked!");
            }
        
            LockHandler.getLockedAppData(lockedAppList_JTable);
        }
    }
    
