    private void chooseApp_JButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chooseApp_JButtonActionPerformed
        // TODO add your handling code here:
        String filePath="";
        chooser = new JSystemFileChooser();
        chooser.setMultiSelectionEnabled(false);
        chooser.setDialogType(JFileChooser.OPEN_DIALOG);
        chooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        chooser.setDialogTitle("Select Application to Lock");
        chooser.setApproveButtonText("Lock App");
        int i=chooser.showDialog(rootPane, null);
        if(i==0)
        {
            filePath=String.valueOf(chooser.getSelectedFile().getAbsoluteFile());
            if(db.checkRecord("LOCKED_APPS_TBL", "APP_LOCATION='"+filePath+"'"))
            {
                Gen_Util.showInformationDialog(rootPane, "App already exists in list!");
                
            }
            else
            {
                if(LockHandler.lockApp(filePath))
                {
                    Gen_Util.showSuccessDialog(rootPane, "Successfully Locked!");
                }
        
                LockHandler.getLockedAppData(lockedAppList_JTable);
                
            }
        }
        
    }//GEN-LAST:event_chooseApp_JButtonActionPerformed

